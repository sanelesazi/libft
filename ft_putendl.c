#include "libft.h"

void	ft_putendl(const char *s)
{
	int		i;
	char	*str;

	if (!s)
		return ;
	str = (char *)s;
	i = 0;
	while (str[i])
	{
		write(1, &str[i], 1);
		i++;
	}
	write(1, "\n", 1);
}
