#include "libft.h"

size_t	ft_strlcat(char *restrict dst, const char *restrict src, size_t size)
{
	size_t	i;
	size_t	len;

	i = 0;
	while (dst[i] && i < size)
		i++;
	len = i;
	if ((size - i) == 0)
		return (i + ft_strlen(src));
	while (src[i - len] && i < size - 1)
	{
		dst[i] = src[i - len];
		i++;
	}
	if (len < size)
		dst[i] = '\0';
	return (len + ft_strlen(src));
}
