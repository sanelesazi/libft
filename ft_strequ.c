int	ft_strequ(const char *s1, const char *s2)
{
	int i;

	if (!s1 || !s2)
		return (0);
	i = 0;
	if (s1[i] == s2[i])
	{
		i = 0;
		while (s1[i] == s2[i])
		{
			if (s1[i + 1] == '\0' && s2[i + 1] == '\0')
				return (1);
			i++;
		}
	}
	else
		return (0);
	return (0);
}
